package com.inseoul.Server_mapdata;

public class Spot {

    private int IDNUM;
    private  double Y;
    private  double X;
    public int getIDNUM() {
        return IDNUM;
    }

    public void setIDNUM(int IDNUM) {
        this.IDNUM = IDNUM;
    }

    public double getY() {
        return Y;
    }

    public void setY(double y) {
        Y = y;
    }

    public double getX() {
        return X;
    }

    public void setX(double x) {
        X = x;
    }




}
